#include <iostream>
using namespace std;

int main() {

  int a = 10;
  char b = 'a';
  bool c = true;
  float d = 20.5;
  double e = 30.5;
  long f = 40;
  short g = 50;
  unsigned int h = 60;
  unsigned long i = 70;
  unsigned short j = 80;
  long long k = 90;
  unsigned long long l = 100;

  cout << "Integer(int): a = " << a << endl;
  cout << "Character(char): b = " << b << endl;
  cout << "Boolean(bool): c = " << c << endl;
  cout << "Float(float): d = " << d << endl;
  cout << "Double(double): e = " << e << endl;
  cout << "Long(long): f = " << f << endl;
  cout << "Short(short): g = " << g << endl;
  cout << "Unsigned Integer(unsigned int): h = " << h << endl;
  cout << "Unsigned Long(unsigned long): i = " << i << endl;
  cout << "Unsigned Short(unsigned short): j = " << j << endl;
  cout << "Long Long(long long): k = " << k << endl;
  cout << "Unsigned Long Long(unsigned long long): l = " << l << endl;

  return 0;
}